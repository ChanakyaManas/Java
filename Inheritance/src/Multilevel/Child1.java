package Multilevel;

public class Child1 extends Parent {
	public void Add(int a,int b) {
		int c=a+b;
		System.out.println(c);
	}
	public void Sub(double a,double b) {
		double c=a+b;
		System.out.println(c);
	}
	public void Multi(double a,double b) {
		double c=a*b;
		System.out.println(c);
	}
	public void Div(double a,double b) {
		double c=a/b;
		System.out.println(c);
	}
}
